const categories = ["PEOPLE", "ORGANIZATIONS", "TOOLS", "PRODUCTS", "ANIMALS", "PRACTICES", "STRUCTURES", "LAWS/INITIATIVES"];

export class Round {
    /**
     * @param {string} category
     * @param {string} word
     * @param {...string} clues
     */
    constructor(category, word, ...clues) {
        this.category = category;
        this.word = word;
        this.clues = clues;

        if (!wordList.includes(word))
            throw Error(`${word} is not a valid word.`);

        if (clues.length != 5)
            throw Error(`Invalid number of clues: expected 5 got ${clues.length}`);

        if (!categories.includes(category))
            throw Error(`${category} is not a valid category.`);

        this.clues = [...clues, word];
    }
}

